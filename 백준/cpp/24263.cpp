#include <iostream>

int main() {
    int input_num;
    std::cin >> input_num;
    std::cout << input_num << std::endl << "1";
    return 0;
}

/*
#include <iostream>을 사용하여 C++의 입출력 스트림을 포함합니다.
std::cout을 사용하여 출력합니다. 이는 C 언어의 printf와 유사합니다.
std::cin을 사용하여 입력을 받습니다. 이는 C 언어의 scanf와 유사합니다.
<< 연산자를 사용하여 출력할 내용을 연결합니다. std::endl은 줄바꿈을 의미합니다.
*/
